    int posicioneliminar;
    private void btn_buscarDELActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarDELActionPerformed
        String numeroCedula = campo_BuscarCedulaDEL.getText();
        boolean usuarioEncontrado = false;
        for (int i = 0; i < this.ventanaMenu.listaPersonas.length; i++) {
            posicioneliminar=i;
            if (this.ventanaMenu.listaPersonas[i] != null && this.ventanaMenu.listaPersonas[i].getCedula().equals(numeroCedula)) {
                etq_buscarDEL.setText("Usuario Encontrado");
                campo_nombreDEL.setText(this.ventanaMenu.listaPersonas[i].getNombres());
                campo_apellidoDEL.setText(this.ventanaMenu.listaPersonas[i].getApellidos());
                campo_cedulaDEL.setText(this.ventanaMenu.listaPersonas[i].getCedula());
                    
                campo_nombreDEL.setEnabled(false);
                campo_apellidoDEL.setEnabled(false);
                campo_cedulaDEL.setEnabled(false);
                    //posicionModificar = i;
  
                usuarioEncontrado = true; // El usuario ha sido encontrado
                break; // Salir del ciclo una vez encontrado
            }
        }

        if (!usuarioEncontrado) {
            etq_buscarDEL.setText("Usuario No Encontrado");
            campo_nombreDEL.setText("");
            campo_apellidoDEL.setText("");
            campo_cedulaDEL.setText("");
        }
          
    }//GEN-LAST:event_btn_buscarDELActionPerformed

