    private void btn_eliminarDELActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarDELActionPerformed
        this.ventanaMenu.listaPersonas[posicioneliminar] = null;

        // Desplazar las siguientes posiciones hacia atrÃ¡s
        for (int i = posicioneliminar + 1; i < this.ventanaMenu.listaPersonas.length; i++) {
            this.ventanaMenu.listaPersonas[i - 1] = this.ventanaMenu.listaPersonas[i];
        }
        this.ventanaMenu.listaPersonas[this.ventanaMenu.listaPersonas.length - 1] = null;
        System.out.println("Usuario Eliminado");
        campo_nombreDEL.setText("");
        campo_apellidoDEL.setText("");
        campo_cedulaDEL.setText("");
        campo_nombreDEL.setEnabled(true);
        campo_apellidoDEL.setEnabled(true);
        campo_cedulaDEL.setEnabled(true);

        etq_buscarDEL.setText("Usuario Eliminado");
    }//GEN-LAST:event_btn_eliminarDELActionPerformed

